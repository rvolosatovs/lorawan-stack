package test

import (
	"fmt"
	"time"

	"github.com/TheThingsNetwork/ttn/pkg/identityserver/types"
	"github.com/smartystreets/assertions"
)

func defaultComponent(in interface{}) (*types.DefaultComponent, error) {
	if u, ok := in.(types.Component); ok {
		return u.GetComponent(), nil
	}

	if d, ok := in.(types.DefaultComponent); ok {
		return &d, nil
	}

	if ptr, ok := in.(*types.DefaultComponent); ok {
		return ptr, nil
	}

	return nil, fmt.Errorf("Expected: '%v' to be of type types.DefaultComponent but it was not", in)
}

// ShouldBeComponent checks if two Components resemble each other
func ShouldBeComponent(actual interface{}, expected ...interface{}) string {
	if len(expected) != 1 {
		return fmt.Sprintf("Expected: one component to match but got %v", len(expected))
	}

	a, s := defaultComponent(actual)
	if s != nil {
		return s.Error()
	}

	b, s := defaultComponent(expected[0])
	if s != nil {
		return s.Error()
	}

	return all(
		ShouldBeComponentIgnoringAutoFields(a, b),
		assertions.ShouldHappenWithin(a.Created, time.Millisecond, b.Created),
	)
}

// ShouldBeComponentIgnoringAutoFields checks if two Components resemble each other
// without looking at fields that are generated by the database: created
func ShouldBeComponentIgnoringAutoFields(actual interface{}, expected ...interface{}) string {
	if len(expected) != 1 {
		return fmt.Sprintf("Expected: one component to match but got %v", len(expected))
	}

	a, s := defaultComponent(actual)
	if s != nil {
		return s.Error()
	}

	b, s := defaultComponent(expected[0])
	if s != nil {
		return s.Error()
	}

	return all(
		assertions.ShouldEqual(a.ID, b.ID),
		assertions.ShouldEqual(a.Type, b.Type),
	)
}
