package oauth

import (
	"net/http"

	"github.com/labstack/echo"
)

// logout is an echo handler that logs the user out.
func (s *Server) logout(c echo.Context) error {
	s.removeCookie(c)

	return c.NoContent(http.StatusOK)
}
